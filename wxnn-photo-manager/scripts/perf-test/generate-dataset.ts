// 套装名样本（模拟真实数据分布）
const OUTFIT_SAMPLES = [
  '星海', '初雪', '夜歌', '晨曦', '暮色', '繁花', '月影', '云霓',
  '蝶舞', '雪绒', '春风', '秋意', '夏梦', '冬韵', '琉璃', '翡翠'
]

// 场景类型样本
const SCENE_SAMPLES = ['室内', '室外', '夜景', '日间', '黄昏', '清晨']

function randomDate(start: Date, end: Date): Date {
  return new Date(start.getTime() + Math.random() * (end.getTime() - start.getTime()))
}

function pad(n: number, len = 2): string {
  return String(n).padStart(len, '0')
}

function formatDate(d: Date): string {
  return `${d.getFullYear()}${pad(d.getMonth() + 1)}${pad(d.getDate())}_${pad(d.getHours())}${pad(d.getMinutes())}${pad(d.getSeconds())}`
}

async function generateDataset(targetDir: string, imageCount: number, videoCount: number): Promise<void> {
  const startTime = Date.now()
  console.log(`[T15] 开始生成数据集: ${imageCount} 张图片 + ${videoCount} 个视频 → ${targetDir}`)

  await fs.promises.mkdir(targetDir, { recursive: true })

  const now = new Date()
  const twoYearsAgo = new Date(now.getFullYear() - 2, now.getMonth(), now.getDate())

  // 生成图片
  let generated = 0
  for (let i = 0; i < imageCount; i++) {
    const date = randomDate(twoYearsAgo, now)
    const outfit = OUTFIT_SAMPLES[i % OUTFIT_SAMPLES.length]
    const scene = SCENE_SAMPLES[i % SCENE_SAMPLES.length]
    const fileName = `${formatDate(date)}_${outfit}_${scene}_${pad(i, 5)}.jpg`
    const filePath = path.join(targetDir, fileName)
    try {
      await fs.promises.writeFile(filePath, MINIMAL_JPEG)
      generated++
      // 修改文件 mtime 为生成的日期，便于按日期筛选测试
      await fs.promises.utimes(filePath, date, date)
    } catch (err) {
      console.error(`生成图片失败 ${fileName}:`, err)
    }
    if ((i + 1) % 1000 === 0) {
      console.log(`[T15] 图片进度: ${i + 1} / ${imageCount}`)
    }
  }

  // 生成视频
  for (let i = 0; i < videoCount; i++) {
    const date = randomDate(twoYearsAgo, now)
    const fileName = `${formatDate(date)}_video_${pad(i, 5)}.mp4`
    const filePath = path.join(targetDir, fileName)
    try {
      await fs.promises.writeFile(filePath, MINIMAL_MP4)
      generated++
      await fs.promises.utimes(filePath, date, date)
    } catch (err) {
      console.error(`生成视频失败 ${fileName}:`, err)
    }
    if ((i + 1) % 1000 === 0) {
      console.log(`[T15] 视频进度: ${i + 1} / ${videoCount}`)
    }
  }

  const elapsed = ((Date.now() - startTime) / 1000).toFixed(2)
  console.log(`[T15] 数据集生成完成: ${generated} 个文件，耗时 ${elapsed}s`)
}

// CLI 入口
const args = process.argv.slice(2)
if (args.length < 1) {
  console.log('用法: npx ts-node --project tsconfig.scripts.json scripts/perf-test/generate-dataset.ts <目标目录> [图片数] [视频数]')
  console.log('示例: npx ts-node --project tsconfig.scripts.json scripts/perf-test/generate-dataset.ts ./perf-data 10000 5000')
  process.exit(0)
}

const targetDir = args[0]
const imageCount = parseInt(args[1] || '10000', 10)
const videoCount = parseInt(args[2] || '5000', 10)

generateDataset(targetDir, imageCount, videoCount).catch((err) => {
  console.error('[T15] 数据集生成失败:', err)
  process.exit(1)
})
